package classes;
/**Classe para objetos do tipo Funcionário, onde serão contidos, valores e métodos para o mesmo
 * @author Wedson e Paulo
 * @since 28/07/2016
 */

public class Funcionario {
    
     /** Método para retorno do nome do funcionário
     * @param nome String - Nome do funcionario 
     * @return String - Nome do funcionario
     */
    
     public String nome(String nome){
         return nome;
     }
     
     /** Método para retorno da idade do funcionário
     * @param idade String - Idade do funcionario
     * @return String - Idade do funcionario
     */
     
     public String idade(String idade){
         return idade;
     }
     
     /** Método para retorno da profissão do funcionário
     * @param profissao String - Profissão do funcionario
     * @return String - Profissão do funcionario
     */
     
     public String profissao(String profissao){
         return profissao;
     }
}
